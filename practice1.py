from turtle import *

def donut():
    circle(30)
    left(90)
    left(180)
    color('white')
    forward(60)
    color('black')
    left(90)
    circle(90)

donut()

def quad():
    forward(100)
    left(90)
    forward(100)
    left(90)
    forward(100)
    left(90)
    forward(100)
    left(90)

quad()

def threeshape():
    forward(100)
    left(120)
    forward(100)
    left(120)
    forward(100)
    left(120)

threeshape()